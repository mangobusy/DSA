# 数据结构：相互之间存在着一种或者多种关系的数据元素的集合和该集合中数据元素之间的关系组成
#         简单来说，数据结构就是设计数据以何种方式组织并储存在计算机中
# 数据结构按照逻辑结构可分为：线性结构、树结构、图结构
#    线性结构：数据结构中的元素存在一对一的相互关系
#    树结构：数据结构中的元素存在一对多的相互关系
#    图结构：数据结构中的元素存在多对多的相互关系
# ——---------------------------------------
# 列表
# -----------------------------------------
# 栈：一个数据集合，只能在一端进行插入或删除——LIFO--Last in first out
# 进栈push   出栈pop   取栈顶gettop
# -----------------------------------------
# 队列（queue）:一个数据集合，只能在一端进行插入 另一端删除--先进先出first in first out